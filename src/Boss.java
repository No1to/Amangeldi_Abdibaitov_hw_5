public class Boss {
    private int bossHp;
    private int bossDmg;
    private String bossDef;

    public int getBossHp() {
        return bossHp;
    }

    public void setBossHp(int bossHp) {
        this.bossHp = bossHp;
    }

    public int getBossDmg() {
        return bossDmg;
    }

    public void setBossDmg(int bossDmg) {
        this.bossDmg = bossDmg;
    }

    public String getBossDef() {
        return bossDef;
    }

    public void setBossDef(String bossDef) {
        this.bossDef = bossDef;
    }
}
